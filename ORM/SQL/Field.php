<?php
class ORM_SQL_Field {
	//private $tableName;
	private $name;
	private $value;
	
	/**
	 * 
	 * @param String $tableName
	 * @param String $fieldName
	 * @param mixed $value 
	 */
	public function __construct($fieldName, $value = null){		
		$this->name = $fieldName;
		$this->value = $value;
	}
	
	public function getTableName(){
		return $this->tableName;
	}
	
	public function getName(){
		return $this->name;
	}
	
	public function getValue(){
		return $this->value;
	}
}